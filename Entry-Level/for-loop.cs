/*Write a program that outputs to the screen "for loop made my day!" 10 times using the for loop*/
using System;
namespace ForLoops
{
	class FirstUsage
	{
		static void Main(string[] args)
		{
			for(int i = 0; i < 10; i++)
			{
				Console.WriteLine("for loop made my day!");
			}
		}
	}
}